package com.vo;

public class ClassVO {
	
	private String classId;
	private String className;
	private String classMaster;
	private String classDescribe;
	
	public String getClassId() {
		return classId;
	}
	public void setClassId(String classId) {
		this.classId = classId;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getClassMaster() {
		return classMaster;
	}
	public void setClassMaster(String classMaster) {
		this.classMaster = classMaster;
	}
	public String getClassDescribe() {
		return classDescribe;
	}
	public void setClassDescribe(String classDescribe) {
		this.classDescribe = classDescribe;
	}
}
