package com.PackageVO;

public class TeacherVO {
	
		private String teacherNum;
		private String teacherName;
		private String teacherSex;
		private String teacherBirth;
		private String teacherWorktime;
		private String teacherAddress;
		private String teacherId;
		
		public String getTeacherNum() {
			return teacherNum;
		}
		public void setTeacherNum(String teacherNum) {
			this.teacherNum = teacherNum;
		}
		public String getTeacherName() {
			return teacherName;
		}
		public void setTeacherName(String teacherName) {
			this.teacherName = teacherName;
		}
		public String getTeacherSex() {
			return teacherSex;
		}
		public void setTeacherSex(String teacherSex) {
			this.teacherSex = teacherSex;
		}
		public String getTeacherBirth() {
			return teacherBirth;
		}
		public void setTeacherBirth(String teacherBirth) {
			this.teacherBirth = teacherBirth;
		}
		public String getTeacherWorktime() {
			return teacherWorktime;
		}
		public void setTeacherWorktime(String teacherWorktime) {
			this.teacherWorktime = teacherWorktime;
		}
		public String getTeacherAddress() {
			return teacherAddress;
		}
		public void setTeacherAddress(String teacherAddress) {
			this.teacherAddress = teacherAddress;
		}
		public void setTeacherId(String teacherId) {
			this.teacherId = teacherId;
		}
		public String getTeacherId() {
			return teacherId;
		}
		
}
